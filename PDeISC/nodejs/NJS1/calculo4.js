// Creamos la función para la suma y la exportamos
export function suma (a,b){
    return (a+b);
}
// Creamos la función para la resta y la exportamos
export function resta (a,b){
    return (a-b);
}
// Creamos la función para la multiplicación y la exportamos
export function multipli (a,b){
    return (a*b);
}
// Creamos la función para la divición y la exportamos
export function dividi (a,b){
    return (a/b);
}